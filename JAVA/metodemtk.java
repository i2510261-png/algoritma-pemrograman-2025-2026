//1. Abs = Mutlak
int nomor = -6;
System.out.println(Math.abs(nomor));

//2. Pow = Pangkat
int nomor = 3;
System.out.println(Math.pow(nomor, 3));

//3. Sqrt = Kuadrat
int nomor = 64;
System.out.println(Math.sqrt(nomor));

//4. Min = Minimal
int angka1 = 2;
int angka2 = 10;
System.out.println(Math.min(angka1, angka2));

//5. Max = Maximal
int angka1 = 15;
int angka2 = 25;
System.out.println(Math.max(angka1, angka2));

//6. a. Round = Dibulatkan ke BAWAH
double angka = 5.4;
System.out.println(Math.round(angka));

//6. b. Round = Dibulatkan ke ATAS
double angka = 5.7;
System.out.println(Math.round(angka));