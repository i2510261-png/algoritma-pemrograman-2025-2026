public class OperatorRelasional {
    
    public static void main(String[] args) {
        
        System.out.println("--- Eksperimen Operator Relasional ---");

        // Operator Sama Dengan (==)
        boolean b1 = (1 == 1);
        System.out.println("1 == 1 : " + b1 + "\n");

        // Operator Tidak Sama Dengan (!=)
        boolean b2 = (2 != 2);
        System.out.println("2 != 2 : " + b2 + "\n");

        // Operator Lebih Dari (>)
        boolean b3 = (5 > 3);
        System.out.println("5 > 3 : " + b3 + "\n");

        // Operator Lebih Dari Sama Dengan (>=)
        boolean b4 = (5 >= 5);
        System.out.println("5 >= 5 : " + b4 + "\n");

        // Operator Lebih Dari Sama Dengan (>=) (Contoh Lain)
        boolean b5 = (5 >= 8);
        System.out.println("5 >= 8 : " + b5 + "\n");

        // Operator Kurang Dari (<)
        boolean b6 = (7 < 10);
        System.out.println("7 < 10 : " + b6 + "\n");

        // Operator Kurang Dari Sama Dengan (<=)
        boolean b7 = (9 <= 9);
        System.out.println("9 <= 9 : " + b7 + "\n");

        // Operator Kurang Dari Sama Dengan (<=) (Contoh Lain)
        boolean b8 = (20 <= 15);
        System.out.println("20 <= 15 : " + b8 + "\n");
        
    }
}