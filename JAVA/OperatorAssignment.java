public class OperatorAssignment {
public static void main(String[] args) {
        int a = 5;
        a += 3;
        System.out.println("Hasil a += 3 adalah " + a);
        a -= 2;
        System.out.println("Hasil a -= 2 adalah " + a);
        a *= 4;
        System.out.println("Hasil a *= 4 adalah " + a);
        a /= 3;
        System.out.println("Hasil a /= 3 adalah " + a);
        a %= 2;
        System.out.println("Hasil a %= 2 adalah " + a);
    }
}
